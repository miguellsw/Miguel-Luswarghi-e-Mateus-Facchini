var diasemana = parseInt(prompt("Digite um número de 1 a 7 para ver o dia da semana correspondente"));

switch (diasemana) {
    case 1:
        alert("Domingo");
        break;

        case 2:
            alert("Segunda-feira")
        break;

        case 3:
            alert("Terça-feira")
            break;

        case 4:
            alert("Quarta-feira");
            break;
        
        case 5:
            alert("Quinta-feira")
            break;
        
        case 6:
            alert("Sexta-feira")
            break;

        case 7:
            alert("Sabado")
    default:
        break;
}